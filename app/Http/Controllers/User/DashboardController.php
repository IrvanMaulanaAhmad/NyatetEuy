     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $id = $request->user()->id;

        $user = User::find($id);
        $trin = TransactionIn::where('user_id', $id)->orderBy('id', 'desc')->limit(5)->get();
        $trintotal = TransactionIn::where('user_id', $id)->count();
        return view('user.dashboard', ['user' => $user, 'trins' => $trin, 'trintotal' => $trintotal]);
    }

